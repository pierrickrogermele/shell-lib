# System library

################################################################################

function get_platform {
		uname | sed 's/^\([A-Za-z]*\).*$$/\1/' | tr '[:upper:]' '[:lower:]'
}

################################################################################

function is_mac {
		[ `get_platform` = "darwin" ]
}

################################################################################

function is_cygwin {
		[ `get_platform` = "cygwin" ]
}

################################################################################

function is_windows {
		is_cygwin
}

################################################################################
# PATH SEPARATOR

if is_windows ; then
		path_sep=';'
else
		path_sep=':'
fi
